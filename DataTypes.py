# All Data Types in Python
x="Hello World"
print(type(x))

x=20
print(type(x))

x=20.5
print(type(x))

# Complex numbers are written with a "j" as the imaginary part
x=5j
print(type(x))

x=["One","Two","Three"]
print(type(x))

x=("One","Two","Three")
print(type(x))

x={"One","Two","Three"}
print(type(x))

x={"Name":"Dishu","Age":21}
print(type(x))

x=range(5)
print(type(x))

x=frozenset({"One","Two","Three"})
print(type(x))

x=False
print(type(x))

x=b"Hello"
print(type(x))

x=bytearray(5)
print(type(x))

x=memoryview(bytes(5))
print(type(x))

x=None
print(type(x))